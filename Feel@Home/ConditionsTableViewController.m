- (void)viewWillAppear:(BOOL)animated
{
    [super viewWillAppear:animated];
    NSIndexPath *path = [NSIndexPath indexPathForRow:[self.conditionsClasses indexOfObject:self.selectedClass] inSection:0];
    [self.tableView selectRowAtIndexPath:path animated:YES scrollPosition:UITableViewScrollPositionTop];
}

